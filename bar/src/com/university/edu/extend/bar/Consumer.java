package com.university.edu.extend.bar;

public class Consumer {

	private int age;
	private int alcohol;

	public Consumer(int age, int alcohol) {

		this.age = age;
		this.alcohol = alcohol;

	}

	public int getAge() {
		return this.age = age;
	}

	public int getAlcohol() {

		return this.alcohol;
	}

}
