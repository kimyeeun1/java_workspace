package homework;

public class Calculator {

	public double addNumber(double numberOne, double numberTwo) {
		return numberOne + numberTwo;
	}

	public double subNumber(double numberOne, double numberTwo) {
		return numberOne - numberTwo;
	}

	public double mulNumber(double numberOne, double numberTwo) {
		return numberOne * numberTwo;
	}

	public double divNumber(double numberOne, double numberTwo) {
		return numberOne / numberTwo;

	}

}
